import turtle as t
screen = t.Screen()
t.shape("turtle")
t.speed(50)
t.setup(width=1200, height=800)
def box():
    t.forward(300)
    t.left(90)
    t.forward(300)
    t.left(90)
    t.forward(300)
    t.left(90)
    t.forward(300)
    t.left(90)
    t.forward(100)
    t.left(90)
    t.forward(300)
    t.right(90)
    t.forward(100)
    t.right(90)
    t.forward(300)
    t.setheading(0)
    t.forward(100)
    t.setheading(90)
    t.forward(200)
    t.setheading(180)
    t.forward(300)
    t.setheading(270)
    t.forward(100)
    t.setheading(0)
    t.forward(300)
    t.penup()
    t.goto(-200,300)
    t.pendown()
box()
def steps():
    t.goto(-550, 300)
    t.write("Welcome to TicTacToe!", font=(50))
    t.goto(-550, 270)
    t.write("Rules for 'X'", font=(50))
    t.goto(-550, 240)
    t.write("1. To draw a 'X' the turtle show be on the bottom", font=(50))
    t.goto(-550, 210)
    t.write("left corner of that particular box", font=(50))
    t.goto(-550, 180)
    t.write("2.Then simply press 'x' on your keyboard in small letters", font=(50))
    t.goto(-550, 150)
    t.write("Rules for '0'", font=(50))
    t.goto(-550, 120)
    t.write("1.To draw a circle on a box, the left side should", font=(50))
    t.goto(-550, 90)
    t.write("be facing to the particular box.", font=(50))
    t.goto(-550, 60)
    t.write("2.Then simply press '0' on keyboard from numpad.", font=(50))
    t.penup()
    t.goto(0,0)
steps()

def esc():
    t.reset()
    t.speed(20)
    box()
    steps()
def down():
    t.penup()
    t.seth(270)
    t.forward(50)
def rigth():
    t.penup()
    t.seth(0)
    t.forward(50)
def left():
    t.penup()
    t.seth(180)
    t.forward(50)
def up():
    t.penup()
    t.seth(90)
    t.forward(50)
def zero():
    t.pendown()
    t.circle(50)
def cross():
    t.pendown()
    cl = t.position()
    t.goto(cl + (100, 100))
    t.seth(180)
    t.forward(100)
    t.seth(315)
    t.forward(142)
    t.seth(0)
screen.listen()
screen.onkey(esc, "BackSpace")
screen.onkey(up, "Up")
screen.onkey(down, "Down")
screen.onkey(rigth, "Right")
screen.onkey(left, "Left")
screen.onkey(zero, "0")
screen.onkey(cross, "x")
t.mainloop()
